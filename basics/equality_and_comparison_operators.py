# Equality and comparison operators

x = 2
y = 2.0
print(f"x = {x}")
print(f"y = {y}")
print(f"x == 2: {x == 2}")  # Checks if x equals 2

print(f"x == y: {x == y}")  # Checks if x equals y (2.0)
print(f"type(x) == type(y): {type(x) == type(y)}")  # Checks if types of x and y match
print()


n = 1
print(f"n = {n}")
print(f"x == n: {x == n}")  # Checks if x equals n (1)
print()


black_sheep = 8
white_sheep = 4

# Comparison operator using basic math
print(f"black_sheep = {black_sheep}")
print(f"white_sheep = {white_sheep}")
print(f"black_sheep == 2 * white_sheep: {black_sheep == 2 * white_sheep}")  # Check if black_sheep equals double of white_sheep

# Using greater than (>) and less than (<) comparison operators
print(f"black_sheep > white_sheep: {black_sheep > white_sheep}")  # Check if black_sheep is greater than white_sheep
print(f"black_sheep < white_sheep: {black_sheep < white_sheep}")  # Check if black_sheep is less than white_sheep
print()


# Using not equal operator (!=)
var = 0
print(f"var = {var}")
print(f"var != 0: {var != 0}")  # Check if var (0) is not equal to 0
print()

var = 1
print(f"var = {var}")
print(f"var != 0: {var != 0}")  # Check if var (1) is not equal to 0
