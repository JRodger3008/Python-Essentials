# A quick reference program that demonstrates how to use Python's built-in syntax
# and functions to work with binary (0b), octal (0o), and hexadecimal (0x) numbers.

print("0b10000000000 =", 0b10000000000) # 1024
print("bin(1024) =", bin(1024)) # 0b10 000 000 000
print("0o653 =", bin(0o653)) # 0b110 101 011
print("0xd12e33 =", 0xd12e33) # 13708851
print("0x1234 =" , 0x1234) # 4660
print("bin(0x1234) =" , bin(0x1234)) # 0b1001000110100
print(f"{90:c}") # Unicode character (Z)
